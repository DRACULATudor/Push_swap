#include "push_swap.h"

int	lenofnums(char **arr)
{
	int i;
	int j;
	int len;

	j = 0;
	len = 0;
	while (arr[j] != NULL)
	{
		i = 0;
		if (arr[j][0] != '\0')
		{
			while (arr[j][i] != '\0')
			{
				if ((arr[j][i] != ' ' && arr[j][i] != '\t' && arr[j][i] != '\n'
						&& arr[j][i] != '\r' && arr[j][i] != '\f'
						&& arr[j][i] != '\v') && (i == 0 || (arr[j][i
							- 1] == ' ' || arr[j][i - 1] == '\t' || arr[j][i
							- 1] == '\n' || arr[j][i - 1] == '\r' || arr[j][i
							- 1] == '\f' || arr[j][i - 1] == '\v')))
					len++;
				i++;
			}
		}
		j++;
	}
	return (len);
}